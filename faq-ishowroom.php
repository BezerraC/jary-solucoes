  <dl>
    <dt><a name="1">1- Porque a opção "Bairro" do menu está inativa?</a></dt>
    <dd>A opção "Bairro" do menu fica inicialmente inativa pois necessita da seleção da
      opção
      "Cidade" para poder ser apresentado os bairros referente aquela cidade.</dd>
    <dt><a name="2">2- Ativei o Modo Offline, mas quando estou sem internet não consigo ver
        os
        imóveis.</a></dt>
    <dd>Isto acontece porque não existem imóveis na base de dados offline. Para atualizar a
      sua
      base de dados offline, na tela inicial, toque no botão "Configurações", depois na
      seção
      "Dados Offline" e então toque em "Atualizar dados". Isso poderá demorar muito tempo
      dependendo da velocidade da sua internet.</dd>
    <dt><a name="3">3- Como faço para imprimir os detalhes de um imóvel ou um resultado de
        uma
        busca?</a></dt>
    </dt>
    <dd>Para imprimir, basta tocar no botão com o símbolo "Impressora" e selecionar na lista
      a
      impressora desejada. Para mais informações sobre impressoras compatíveis com o iPad,
      acesse <a href="http://support.apple.com/kb/HT4356?viewlocale=pt_BR" title="Clique para acessar">http://support.apple.com/kb/HT4356?viewlocale=pt_BR</a>.
    </dd>
