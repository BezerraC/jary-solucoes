  <dl>
    <dt><a name="1">1- Como faço para alterar as informações do bebê, como nome, data de
        nascimento ou foto?</a></dt>
    <dd>Na tela inicial do app, toque no botão "Editar" e então selecione o bebê, em
      seguida,
      aparecerá uma nova tela mostrando as informações atuais. Agora, basta editar as
      informações desejadas e tocar em "OK".</dd>
    <dt><a name="2">2- Porque não consigo visualizar os gráficos dos registros do meu
        bebê?</a>
    </dt>
    <dd>É necessário ter no mínimo 3 registros no período selecionado para que os gráficos
      possam ser gerados.</dd>
    <dt><a name="3">3- Como faço para salvar uma cópia de segurança dos meus dados salvos no
        app?</a></dt>
    <dd>Na tela de seleção dos bebês (a tela inicial do app), toque no botão "Configurações"
      e
      depois selecione a opção "Cópia de Segurança". Nesta tela, toque no botão de ação,
      posicionado no canto direito superior, para criar uma nóva cópia de segurança.
      Depois,
      conecte seu iPhone usando o cabo USB ao seu computador e abra o iTunes. Selecione o
      seu
      iPhone na lista de dispositivos que aparece no menu lateral esquerdo, ao selecionar,
      abrirá a tela mostrando a visão geral do seu iPhone. Nesta tela, na parte superior,
      selecione a aba "Apps". Nesta aba, procure a seção "Compartilhamento de Arquivos",
      nela
      haverá uma lista com apps que no iPhone que compartilham arquivos com o iTunes.
      Selecione o BabyNotes e você verá sua lista de cópias de segurança, podendo salva-la
      para restaurar os dados posteriormente caso necessário.</dd>
    <dt><a name="4">4- Como faço para restaurar os dados da minha cópia de segurança?</a>
    </dt>
    <dd>Conecte seu iPhone usando o cabo USB ao seu computador e abra o iTunes. Selecione o
      seu
      iPhone na lista de dispositivos que aparece no menu lateral esquerdo, ao selecionar,
      abrirá a tela mostrando a visão geral do seu iPhone. Nesta tela, na parte superior,
      selecione a aba "Apps". Nesta aba, procure a seção "Compartilhamento de Arquivos",
      nela
      haverá uma lista com apps que no iPhone que compartilham arquivos com o iTunes.
      Selecione o BabyNotes na lista e selecione o botão "Adicionar...". Selecione a cópia
      de
      segurança. Agora sua cópia de segurança está no iPhone. Pronto, tudo que você
      precisa
      fazer agora é ir em "Configurações" (na tela inicial), depois na seção "Cópia de
      segurança" e tocar na cópia de segurança que deseja restaurar e confirmar.</dd>
